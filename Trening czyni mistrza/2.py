# Calculation of circle area and circumference 

# determine radius and PI values
PI = 3.14159
radius = [1, 3]
# calculate area 
calculate_area = PI * radius * radius
# calculate circumference
calculate_circumference = 2 * PI * radius
# print results
print(f'radius: {radius}')
print(f'circumference: {calculate_circumference:.2f}')
print(f'area: {calculate_area:.2f}')